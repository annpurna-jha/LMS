    // validation
    public boolean validateLogin(){
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Username");
            return false;
        }
        if(pwd.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Password");
            return false;
        } 
        return true;
    }
    
    //verify credential
    public void login(){
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        int displayTime = 1000; //time for closing the pop up msg in miliseconds
         try{
            Connection con = DBConnection.getConnection();
            PreparedStatement pst= con.prepareStatement("select * from users where name = ? and password = ?"); 
            pst.setString(1,name);
            pst.setString(2,pwd);
            ResultSet rs= pst.executeQuery();
             if (rs.next()) {
                 //create a pop-up msg
            JOptionPane pane = new JOptionPane("Login Successfully", JOptionPane.INFORMATION_MESSAGE);
            //dialog gives title 
            JDialog dialog = pane.createDialog("Information");
            Timer timer = new Timer(displayTime, e -> { //timer is set for 3sec
                dialog.dispose(); // Close the pop-up and open home page
                HomePage home = new HomePage();
                home.setVisible(true);
                this.dispose();
            });
            
            timer.setRepeats(false); // Only fire the timer once
            timer.start();

            dialog.setVisible(true);
        } else{
               JOptionPane.showMessageDialog(this, "Incorrect Username or Password"); 
            }
         }catch(Exception e){
            e.printStackTrace();
        }
    }
   
 
    

